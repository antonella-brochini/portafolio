export function getProjectMeta(idOrSlug) {
  const key = String(idOrSlug);
  return projectsMeta.find(
    (p) => String(p.id) === key || String(p.slug) === key,
  );
}

export function getProjectSlug(project) {
  return project?.slug || String(project?.id);
}
